const CACHE_NAME = 'panchangam-v2';
const STATIC_ASSETS = [
  '/',
  '/manifest.json'
];

// Endpoints that should never be cached (time-sensitive data)
const NO_CACHE_PATTERNS = [
  '/api/panchang/today',
  '/api/calendar/'
];

self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => {
      return cache.addAll(STATIC_ASSETS);
    })
  );
  self.skipWaiting();
});

self.addEventListener('activate', (event) => {
  event.waitUntil(
    caches.keys().then((cacheNames) => {
      return Promise.all(
        cacheNames
          .filter((name) => name !== CACHE_NAME)
          .map((name) => caches.delete(name))
      );
    })
  );
  self.clients.claim();
});

self.addEventListener('fetch', (event) => {
  if (event.request.method !== 'GET') {
    return;
  }

  const url = new URL(event.request.url);
  
  // Never cache time-sensitive panchang data - always fetch fresh
  const shouldNotCache = NO_CACHE_PATTERNS.some(pattern => url.pathname.includes(pattern));
  
  if (url.pathname.startsWith('/api/')) {
    if (shouldNotCache) {
      // Network only for time-sensitive data
      event.respondWith(fetch(event.request));
      return;
    }
    
    // Network first, cache fallback for other API calls
    event.respondWith(
      fetch(event.request)
        .then((response) => {
          const responseClone = response.clone();
          caches.open(CACHE_NAME).then((cache) => {
            cache.put(event.request, responseClone);
          });
          return response;
        })
        .catch(() => {
          return caches.match(event.request);
        })
    );
    return;
  }

  event.respondWith(
    caches.match(event.request).then((cachedResponse) => {
      if (cachedResponse) {
        fetch(event.request).then((response) => {
          caches.open(CACHE_NAME).then((cache) => {
            cache.put(event.request, response);
          });
        });
        return cachedResponse;
      }

      return fetch(event.request).then((response) => {
        if (response.ok) {
          const responseClone = response.clone();
          caches.open(CACHE_NAME).then((cache) => {
            cache.put(event.request, responseClone);
          });
        }
        return response;
      });
    })
  );
});
